function flickerAnimations() {
    document.getElementById('blue-state-icon').setAttribute('class', 'flickerBlue');
    document.getElementById('red-state-icon').setAttribute('class', 'flickerRed');
    document.getElementById('yellow-state-icon').setAttribute('class', 'flickerYellow');
    document.getElementById('green-state-icon').setAttribute('class', 'flickerGreen');
}